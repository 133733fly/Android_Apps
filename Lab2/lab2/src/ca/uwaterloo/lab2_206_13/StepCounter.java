package ca.uwaterloo.lab2_206_13;

public class StepCounter {
	private static int steps;
	private static int steps2;
	private static long start;
	private static long duration;
	
	public static int steps(){return steps;}
	
	public static void increment(){
		steps++;
		
	}
	
	public static void reset(){
		steps = 0;
		steps2 = 0;
	}

	public static long getStart() {
		return start;
	}

	public static void setStart(long start) {
		StepCounter.start = start;
	}

	public static long getDuration() {
		return duration;
	}

	public static void setDuration(long duration) {
		StepCounter.duration = duration;
	}

	public static int getSteps2() {
		return steps2;
	}

	public static void setSteps2(int steps2) {
		StepCounter.steps2 = steps2;
	}
	
	public static void incrementSteps2(){
		steps2++;
	}
	
}
